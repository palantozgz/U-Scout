function buildBundle(prefixRegex, name, { toM, toN }) {
  const esMap = parse(esBlock, prefixRegex);
  const enMap = parse(enBlock, prefixRegex);
  const zhMap = parse(zhBlock, prefixRegex);
  const keys = Object.keys(esMap).sort();
  const outEn = {};
  const outEs = {};
  const outZh = {};
  for (const k of keys) {
    const baseEs = esMap[k];
    const baseEn = enMap[k] || baseEs;
    const baseZh = zhMap[k] || baseEn;
    outEs[k + "__es_n"] = toN(baseEs);
    outEs[k + "__es_m"] = toM(baseEs);
    outEs[k + "__es_f"] = baseEs;
    outEn[k + "__es_n"] = baseEn;
    outEn[k + "__es_m"] = baseEn;
    outEn[k + "__es_f"] = baseEn;
    outZh[k + "__es_n"] = baseZh;
    outZh[k + "__es_m"] = baseZh;
    outZh[k + "__es_f"] = baseZh;
  }
  emit(name, outEn, outEs, outZh);
}

