function emit(moduleFileBase, outEn, outEs, outZh) {
  const [enName, esName, zhName] = EXPORT_NAMES[moduleFileBase];
  let ts = `/** Auto-generated by scripts/regenerate-spanish-gender-bundles.mjs — do not edit. */\n`;
  ts += `export const ${enName}: Record<string, string> = ${JSON.stringify(outEn, null, 2)};\n\n`;
  ts += `export const ${esName}: Record<string, string> = ${JSON.stringify(outEs, null, 2)};\n\n`;
  ts += `export const ${zhName}: Record<string, string> = ${JSON.stringify(outZh, null, 2)};\n`;
  fs.writeFileSync(path.join(root, `client/src/lib/${moduleFileBase}.ts`), ts);
}

