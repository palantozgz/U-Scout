import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const i18nPath = path.join(root, "client/src/lib/i18n.ts");
const t = fs.readFileSync(i18nPath, "utf8");

const esBlock = t.slice(t.indexOf("const es: I18nStrings = {"), t.indexOf("// ─── CHINESE"));
const enBlock = t.slice(t.indexOf("const en = {"), t.indexOf("const es:"));
const zhBlock = t.slice(t.indexOf("const zh: I18nStrings = {"), t.indexOf("\n};\n\n// ─── Runtime"));

function parse(block, prefixRegex) {
  const map = {};
  const keyRe = new RegExp(`^\\s+(${prefixRegex}):\\s*"((?:\\\\.|[^"\\\\])*)"`, "gm");
  let m;
  while ((m = keyRe.exec(block))) {
    map[m[1]] = m[2].replace(/\\"/g, '"').replace(/\\n/g, "\n");
  }
  return map;
}

